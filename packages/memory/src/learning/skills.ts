/**
 * Latest version of each skill (by title, per agent). Skip rows that have
 * been superseded by a newer `version`.
 */
export async function listLatestSkills(
  db: HipppoDb,
  projectId: string,
  agentId: string,
): Promise<Skill[]> {
  const rows = await db
    .select()
    .from(skills)
    .where(and(eq(skills.projectId, projectId), eq(skills.agentId, agentId)))
    .orderBy(desc(skills.version));

  const latestByTitle = new Map<string, Skill>();
  for (const r of rows) {
    if (!latestByTitle.has(r.title)) latestByTitle.set(r.title, r);
  }
  return [...latestByTitle.values()];
}

/** Increment `timesUsed` and update success rate. Call when a skill is applied. */
export async function recordSkillUse(
  db: HipppoDb,
  skillId: string,
  succeeded: boolean,
): Promise<void> {
  const [row] = await db.select().from(skills).where(eq(skills.id, skillId)).limit(1);
  if (!row) return;
  const newTimesUsed = row.timesUsed + 1;
  const successes = row.successRate * row.timesUsed + (succeeded ? 1 : 0);
  const newSuccessRate = successes / newTimesUsed;
  await db
    .update(skills)
    .set({ timesUsed: newTimesUsed, successRate: newSuccessRate })
    .where(eq(skills.id, skillId));
}

/** For tests / callbacks that want the vector as a string. */
export function encodeVector(v: Float32Array): string {
  return serializeEmbedding(v);
}

export function decodeVector(raw: string): Float32Array {
  return deserializeEmbedding(raw);
}
